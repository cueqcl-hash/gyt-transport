create table if not exists gyt_vehicle_safety (
  id bigserial primary key,
  plate text not null,
  period text not null,          -- 'YYYY-MM'
  trips int,
  km numeric,
  max_speed numeric,             -- ความเร็วสูงสุด กม/ชม
  spd_evt int,                   -- จำนวนครั้งวิ่งเกิน 90
  spd_min int,                   -- นาทีที่วิ่งเกิน 90
  harsh int,                     -- เบรกแรง+ออกตัวแรง+เลี้ยวแรง รวม
  spd_per100 numeric,            -- ครั้งเกิน90 ต่อ 100 กม
  created_at timestamptz default now()
);
alter table gyt_vehicle_safety disable row level security;
create unique index if not exists ux_vehicle_safety on gyt_vehicle_safety(plate, period);

